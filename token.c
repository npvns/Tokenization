void forward_rand(int* out, int* rout, int len)
{
        char buf[4];
	unsigned int myrandom = 0, temp = 0, index = 0;
	struct timeb      timevalue;

	for(index = 0; index < len; index++){ out[index] = index; }
	
	for(index = 0; index < 1000; ++index)
	{
	   ftime(&timevalue);
	   buf[0] = (timevalue.millitm & 0xF);
           buf[2] = (timevalue.millitm & 0xF0) >> 4;
           buf[3] = (timevalue.millitm & 0xF00) >> 8;
           buf[1] = (timevalue.millitm & 0xF000) >> 12;
           RAND_add(buf, sizeof buf, 0.1);	 
        }
   for(index = 0; index < len; index++)
   {
     RAND_bytes(&myrandom, sizeof(unsigned int));
     myrandom = myrandom % len;
     myrandom = myrandom + index;
     if(myrandom > len-1) { myrandom = myrandom - len; }
     temp = out[index]; out[index] = out[myrandom]; out[myrandom] = temp;
   }
  for(index = 0; index < len; index++)
  {
    rout[out[index]] = index;
  }

}

void pick_table(char* key)
{
   int k = 0, i = 0, j = 0;
   memset(tab, 0, 128);
    for(j = 0; j < 16; j++)
    {
     char ch = key[j];
      for( i = 0; i < 8; i++)
      {
        if(ch & 0x80)
        {
          tab[k] = j*8+i;
          k++;
        }
        ch = ch << 1;
      }
    }
    tab_count = k;
}
  

